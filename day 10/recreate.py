def add_to_tuple(input_tuple, item):
    new_tuple = input_tuple + (item,)
    return new_tuple

# Example usage:
original_tuple = (1, 2, 3, 4)
new_item = 5
new_tuple = add_to_tuple(original_tuple, new_item)
print("Original Tuple:", original_tuple)
print("New Tuple:", new_tuple)
