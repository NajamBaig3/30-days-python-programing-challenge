# Initial values of the variables
x = 5
y = 10

# Displaying the initial values
print("Before swapping:")
print("x =", x)
print("y =", y)

# Swapping the values using tuple unpacking
x, y = y, x

# Displaying the swapped values
print("\nAfter swapping:")
print("x =", x)
print("y =", y)
